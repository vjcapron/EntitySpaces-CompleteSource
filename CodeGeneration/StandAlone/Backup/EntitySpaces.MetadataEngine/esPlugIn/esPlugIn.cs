        /// Employees.
        /// </remarks>
        public string DownStreamObjectType(TableRelation tr)
        {
            if (tr.IsOneToOne && !tr.IsDirect)
            {
                return this.Entity(tr.ForeignTable);
            }

            if (tr.IsZeroToMany)
            {
                return this.Entity(tr.ForeignTable);
            }

