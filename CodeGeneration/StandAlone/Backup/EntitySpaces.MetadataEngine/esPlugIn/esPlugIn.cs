        /// </remarks>
        public string UpStreamObjectType(TableRelation tr)
        {
            if (tr.IsOneToOne && tr.IsDirect)
            {
                return this.Entity(tr.ForeignTable);
            }

            if (tr.IsManyToMany)
            {
                if (tr.ForeignTable.PrimaryKeys.Count == 2)
                {
                    return this.Entity(tr.CrossReferenceTable);
                }
            }

            if (tr.IsManyToOne)
            {
                return this.Entity(tr.ForeignTable);
            }

            return null;
        }

